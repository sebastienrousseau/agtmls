#!/usr/bin/env python3
"""Small command dispatcher for AgtMLS maintenance tasks."""

from __future__ import annotations

import argparse
import json
import subprocess
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent


def run(argv: list[str], cwd: Path = ROOT) -> int:
    return subprocess.call(argv, cwd=cwd)


def agent_paths(agent: str) -> tuple[str, str]:
    if agent == "claude":
        return ".claude", "CLAUDE.md"
    if agent == "aider":
        return ".aider", "CONVENTIONS.md"
    if agent == "codex":
        return ".codex", "AGENTS.md"
    raise ValueError(agent)


def uninstall(target: Path, agent: str, remove_prompt: bool) -> int:
    target = target.resolve()
    dot, prompt = agent_paths(agent)
    removed = 0
    for sub in ["skills", "commands"]:
        directory = target / dot / sub
        if not directory.exists():
            continue
        for entry in directory.iterdir():
            if entry.is_symlink():
                try:
                    resolved = entry.resolve(strict=True)
                except FileNotFoundError:
                    resolved = Path("")
                if str(resolved).startswith(str(ROOT)):
                    entry.unlink()
                    removed += 1
    prompt_path = target / prompt
    if remove_prompt and prompt_path.exists():
        first = prompt_path.read_text(encoding="utf-8", errors="replace").splitlines()[:1]
        if first and "Generated by AgtMLS" in first[0]:
            prompt_path.unlink()
            removed += 1
    print(f"removed {removed} AgtMLS-managed item(s) from {target}")
    return 0


def load_index() -> dict[str, object]:
    return json.loads((ROOT / "index.json").read_text(encoding="utf-8"))


def list_entries(kind: str, bundle: str | None, json_output: bool) -> int:
    index = load_index()
    if kind == "commands":
        entries = index.get("commands", [])
        if json_output:
            print(json.dumps(entries, indent=2, sort_keys=True))
            return 0
        for command in entries:
            print(f"command/{command['name']}  {command['description']}")
        return 0
    skills = index["skills"]
    if bundle:
        skills = [skill for skill in skills if (skill["bundle"] or "_general") == bundle]
    if json_output:
        print(json.dumps(skills, indent=2, sort_keys=True))
        return 0
    for skill in skills:
        prefix = skill["bundle"] or "general"
        print(f"{prefix}/{skill['name']}  {', '.join(skill['tags'])}")
    return 0


def all_entries(index: dict[str, object]) -> list[dict[str, object]]:
    entries: list[dict[str, object]] = []
    for skill in index.get("skills", []):
        item = dict(skill)
        item["entry_type"] = "skill"
        entries.append(item)
    for command in index.get("commands", []):
        item = dict(command)
        item["entry_type"] = "command"
        entries.append(item)
    return entries


def search_entries(query: str, json_output: bool) -> int:
    terms = [term.lower() for term in query.split() if term.strip()]
    entries = []
    for entry in all_entries(load_index()):
        haystack = " ".join(
            str(entry.get(key, ""))
            for key in ["name", "description", "bundle", "path", "tags", "entry_type"]
        ).lower()
        if all(term in haystack for term in terms):
            entries.append(entry)
    if json_output:
        print(json.dumps(entries, indent=2, sort_keys=True))
        return 0
    for entry in entries:
        prefix = entry["entry_type"]
        if entry["entry_type"] == "skill":
            prefix = entry.get("bundle") or "general"
        print(f"{prefix}/{entry['name']}  {entry.get('description', '')}")
    return 0


def show_entry(name: str, json_output: bool) -> int:
    matches = [
        entry for entry in all_entries(load_index())
        if entry.get("name") == name or f"{entry.get('bundle')}/{entry.get('name')}" == name
    ]
    if not matches:
        print(f"no registry entry named {name!r}", file=sys.stderr)
        return 1
    entry = matches[0]
    if json_output:
        print(json.dumps(entry, indent=2, sort_keys=True))
        return 0
    print(f"{entry['entry_type']}: {entry['name']}")
    print(f"path: {entry.get('path', '')}")
    if entry.get("bundle"):
        print(f"bundle: {entry['bundle']}")
    if entry.get("tags"):
        print(f"tags: {', '.join(entry['tags'])}")
    if entry.get("description"):
        print()
        print(entry["description"])
    return 0


def stats(json_output: bool) -> int:
    index = load_index()
    payload = {
        "registry_version": index.get("registry_version"),
        "skills": index.get("skill_count", 0),
        "commands": index.get("command_count", 0),
        "bundles": index.get("bundles", {}),
        "coverage": index.get("coverage", {}),
        "quality": index.get("quality", {}),
    }
    if json_output:
        print(json.dumps(payload, indent=2, sort_keys=True))
        return 0
    print(f"registry: {payload['registry_version']}")
    print(f"skills: {payload['skills']}")
    print(f"commands: {payload['commands']}")
    for name, count in sorted(payload["bundles"].items()):
        label = "general" if name == "_general" else name
        print(f"bundle/{label}: {count}")
    quality = payload.get("quality", {})
    if quality:
        print(f"quality: {quality.get('average_score', 0)}")
    coverage = payload["coverage"]
    for name in sorted(coverage):
        item = coverage[name]
        print(f"{name} coverage: {item.get('covered', 0)}/{item.get('total', 0)}")
    return 0


def profiles(json_output: bool) -> int:
    data = json.loads((ROOT / "profiles.json").read_text(encoding="utf-8"))["profiles"]
    if json_output:
        print(json.dumps(data, indent=2, sort_keys=True))
        return 0
    for name, item in sorted(data.items()):
        bundles = ",".join(item.get("bundles", [])) or "general"
        print(f"{name}  bundles={bundles}  {item.get('description', '')}")
    return 0


def providers(json_output: bool) -> int:
    data = json.loads((ROOT / "providers.json").read_text(encoding="utf-8"))
    if json_output:
        print(json.dumps(data, indent=2, sort_keys=True))
        return 0
    for name in sorted(data.get("native_agents", {})):
        print(f"native/{name}")
    for name in sorted(data.get("export_targets", {})):
        print(f"export/{name}")
    return 0


def main() -> int:
    parser = argparse.ArgumentParser(prog="agtmls")
    sub = parser.add_subparsers(dest="command", required=True)

    doctor = sub.add_parser("doctor")
    doctor.add_argument("--target", type=Path)
    doctor.add_argument("--agent", choices=["claude", "aider", "codex"])
    doctor.add_argument("--skills-only", action="store_true")
    doctor.add_argument("--bundle", action="append", default=[])

    status = sub.add_parser("status")
    status.add_argument("--target", type=Path)
    status.add_argument("--agent", choices=["claude", "aider", "codex"])
    status.add_argument("--skills-only", action="store_true")
    status.add_argument("--bundle", action="append", default=[])

    sub.add_parser("check")

    list_cmd = sub.add_parser("list")
    list_cmd.add_argument("kind", nargs="?", choices=["skills", "commands"], default="skills")
    list_cmd.add_argument("--bundle")
    list_cmd.add_argument("--json", action="store_true")

    search = sub.add_parser("search")
    search.add_argument("query")
    search.add_argument("--json", action="store_true")

    show = sub.add_parser("show")
    show.add_argument("name")
    show.add_argument("--json", action="store_true")

    stats_cmd = sub.add_parser("stats")
    stats_cmd.add_argument("--json", action="store_true")

    profiles_cmd = sub.add_parser("profiles")
    profiles_cmd.add_argument("--json", action="store_true")

    providers_cmd = sub.add_parser("providers")
    providers_cmd.add_argument("--json", action="store_true")

    export_cmd = sub.add_parser("export")
    export_cmd.add_argument("--provider", default="generic")
    export_cmd.add_argument("--profile")
    export_cmd.add_argument("--bundle", action="append", default=[])
    export_cmd.add_argument("--out-dir", type=Path)

    docs_site = sub.add_parser("docs-site")
    docs_site.add_argument("--write", action="store_true")
    docs_site.add_argument("--check", action="store_true")

    release_pack = sub.add_parser("release-pack")
    release_pack.add_argument("--out-dir", type=Path)
    release_pack.add_argument("--profile")
    release_pack.add_argument("--provider", action="append", default=[])

    evolve = sub.add_parser("evolve")
    evolve.add_argument("transcript", type=Path)
    evolve.add_argument("--skill-name", required=True)

    evidence = sub.add_parser("evidence")
    evidence.add_argument("--skill", required=True)
    evidence.add_argument("--command", action="append", default=[])
    evidence.add_argument("--file", action="append", default=[])
    evidence.add_argument("--outcome", default="recorded")

    agent_card = sub.add_parser("agent-card")
    agent_card.add_argument("--write", action="store_true")
    agent_card.add_argument("--check", action="store_true")

    mcp_resources = sub.add_parser("mcp-resources")
    mcp_resources.add_argument("--write", action="store_true")
    mcp_resources.add_argument("--check", action="store_true")

    sbom = sub.add_parser("sbom")
    sbom.add_argument("--write", action="store_true")
    sbom.add_argument("--check", action="store_true")

    provenance = sub.add_parser("provenance")
    provenance.add_argument("--write", action="store_true")
    provenance.add_argument("--check", action="store_true")

    provider_install = sub.add_parser("provider-install")
    provider_install.add_argument("--provider", required=True)
    provider_install.add_argument("--target", type=Path, required=True)
    provider_install.add_argument("--profile")
    provider_install.add_argument("--check", action="store_true")

    sub.add_parser("bench")

    diff_cmd = sub.add_parser("diff")
    diff_cmd.add_argument("--from", dest="old", required=True)
    diff_cmd.add_argument("--to", dest="new", default="index.json")
    diff_cmd.add_argument("--json", action="store_true")

    sub.add_parser("release-check")

    import_cmd = sub.add_parser("import-skill")
    import_cmd.add_argument("source", type=Path)
    import_cmd.add_argument("--name")
    import_cmd.add_argument("--bundle")

    index = sub.add_parser("index")
    index.add_argument("--write", action="store_true")
    index.add_argument("--check", action="store_true")

    install = sub.add_parser("install")
    install.add_argument("language")
    install.add_argument("agent", choices=["claude", "aider", "codex"])
    install.add_argument("--target", type=Path, default=Path.cwd())
    install.add_argument("--skills-only", action="store_true")
    install.add_argument("--bundle", action="append", default=[])
    install.add_argument("--profile")

    remove = sub.add_parser("uninstall")
    remove.add_argument("agent", choices=["claude", "aider", "codex"])
    remove.add_argument("--target", type=Path, default=Path.cwd())
    remove.add_argument("--remove-prompt", action="store_true")

    propose = sub.add_parser("propose-skill")
    propose.add_argument("transcript", type=Path)
    propose.add_argument("--skill-name", required=True)

    scaffold = sub.add_parser("scaffold-skill")
    scaffold.add_argument("name")
    scaffold.add_argument("--bundle")
    scaffold.add_argument("--title")

    args = parser.parse_args()

    if args.command in {"doctor", "status"}:
        cmd = [sys.executable, str(ROOT / "scripts" / "agtmls-doctor.py")]
        if args.target:
            cmd.extend(["--target", str(args.target)])
        if args.agent:
            cmd.extend(["--agent", args.agent])
        if args.skills_only:
            cmd.append("--skills-only")
        for bundle in args.bundle:
            cmd.extend(["--bundle", bundle])
        return run(cmd)
    if args.command == "check":
        return run([sys.executable, str(ROOT / "scripts" / "run-all-checks.py")])
    if args.command == "list":
        return list_entries(args.kind, args.bundle, args.json)
    if args.command == "search":
        return search_entries(args.query, args.json)
    if args.command == "show":
        return show_entry(args.name, args.json)
    if args.command == "stats":
        return stats(args.json)
    if args.command == "profiles":
        return profiles(args.json)
    if args.command == "providers":
        return providers(args.json)
    if args.command == "export":
        cmd = [sys.executable, str(ROOT / "scripts" / "export-registry.py"), "--provider", args.provider]
        if args.profile:
            cmd.extend(["--profile", args.profile])
        for bundle in args.bundle:
            cmd.extend(["--bundle", bundle])
        if args.out_dir:
            cmd.extend(["--out-dir", str(args.out_dir)])
        return run(cmd)
    if args.command == "docs-site":
        flags = ["--write"] if args.write else ["--check"] if args.check else []
        return run([sys.executable, str(ROOT / "scripts" / "generate-docs-site.py"), *flags])
    if args.command == "release-pack":
        cmd = [sys.executable, str(ROOT / "scripts" / "release-pack.py")]
        if args.out_dir:
            cmd.extend(["--out-dir", str(args.out_dir)])
        if args.profile:
            cmd.extend(["--profile", args.profile])
        for provider in args.provider:
            cmd.extend(["--provider", provider])
        return run(cmd)
    if args.command == "evolve":
        return run([sys.executable, str(ROOT / "scripts" / "evolve-session.py"), str(args.transcript), "--skill-name", args.skill_name])
    if args.command == "evidence":
        cmd = [sys.executable, str(ROOT / "scripts" / "record-evidence.py"), "--skill", args.skill, "--outcome", args.outcome]
        for item in args.command:
            cmd.extend(["--command", item])
        for item in args.file:
            cmd.extend(["--file", item])
        return run(cmd)
    if args.command == "agent-card":
        flags = ["--write"] if args.write else ["--check"] if args.check else []
        return run([sys.executable, str(ROOT / "scripts" / "generate-agent-card.py"), *flags])
    if args.command == "mcp-resources":
        flags = ["--write"] if args.write else ["--check"] if args.check else []
        return run([sys.executable, str(ROOT / "scripts" / "generate-mcp-resources.py"), *flags])
    if args.command == "sbom":
        flags = ["--write"] if args.write else ["--check"] if args.check else []
        return run([sys.executable, str(ROOT / "scripts" / "generate-sbom.py"), *flags])
    if args.command == "provenance":
        flags = ["--write"] if args.write else ["--check"] if args.check else []
        return run([sys.executable, str(ROOT / "scripts" / "generate-provenance.py"), *flags])
    if args.command == "provider-install":
        cmd = [sys.executable, str(ROOT / "scripts" / "provider-install.py"), "--provider", args.provider, "--target", str(args.target)]
        if args.profile:
            cmd.extend(["--profile", args.profile])
        if args.check:
            cmd.append("--check")
        return run(cmd)
    if args.command == "bench":
        return run([sys.executable, str(ROOT / "scripts" / "bench.py")])
    if args.command == "diff":
        cmd = [sys.executable, str(ROOT / "scripts" / "registry-diff.py"), "--from", args.old, "--to", args.new]
        if args.json:
            cmd.append("--json")
        return run(cmd)
    if args.command == "release-check":
        return run([sys.executable, str(ROOT / "scripts" / "release-check.py")])
    if args.command == "import-skill":
        cmd = [sys.executable, str(ROOT / "scripts" / "import-skill.py"), str(args.source)]
        if args.name:
            cmd.extend(["--name", args.name])
        if args.bundle:
            cmd.extend(["--bundle", args.bundle])
        return run(cmd)
    if args.command == "index":
        flags = ["--write"] if args.write else ["--check"] if args.check else []
        return run([sys.executable, str(ROOT / "scripts" / "generate-skill-index.py"), *flags])
    if args.command == "install":
        cmd = [str(ROOT / "scripts" / "setup-workspace.sh"), args.language, args.agent]
        if args.skills_only:
            cmd.append("--skills-only")
        bundles = list(args.bundle)
        if args.profile:
            data = json.loads((ROOT / "profiles.json").read_text(encoding="utf-8"))["profiles"]
            profile = data.get(args.profile)
            if profile is None:
                print(f"unknown profile: {args.profile}", file=sys.stderr)
                return 1
            bundles.extend(profile.get("bundles", []))
        for bundle in sorted(set(bundles)):
            cmd.extend(["--bundle", bundle])
        return run(cmd, cwd=args.target.resolve())
    if args.command == "uninstall":
        return uninstall(args.target, args.agent, args.remove_prompt)
    if args.command == "propose-skill":
        return run(
            [
                sys.executable,
                str(ROOT / "scripts" / "propose-skill-from-session.py"),
                str(args.transcript),
                "--skill-name",
                args.skill_name,
            ]
        )
    if args.command == "scaffold-skill":
        cmd = [sys.executable, str(ROOT / "scripts" / "scaffold-skill.py"), args.name]
        if args.bundle:
            cmd.extend(["--bundle", args.bundle])
        if args.title:
            cmd.extend(["--title", args.title])
        return run(cmd)
    return 2


if __name__ == "__main__":
    sys.exit(main())
